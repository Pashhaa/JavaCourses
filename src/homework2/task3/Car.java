package homework2.task3;

public class Car {
    int year;
    double speed;
    int weight;
    String colour;

    Car(){ }

    Car (int year){
        this.year=year;
    }

    Car(int year, double speed){
        this.year=year;
        this.speed=speed;
    }

    Car(int year, double speed, int weight){
        this.year=year;
        this.speed=speed;
        this.weight=weight;
    }

    Car(int year, double speed, int weight, String colour){
        this.year=year;
        this.speed=speed;
        this.weight=weight;
        this.colour=colour;
    }





}
